package au.com.gaiaresources.bdrs.model.report.impl;

/**
 * The <code>ReportView</code> represents the views where the report may be optionally generated.
 */
public enum ReportView {
    /**
     * Reports shall be visible on the report listing page.
     */
    REPORT_LISTING,
    /**
     * Reports shall be visible on the advanced review page.
     */
    ADVANCED_REVIEW
}
